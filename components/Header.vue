  <header>
    <h1>{{ title }}</h1>
    <Button 
      v-show="homepage"
      @btn-click="$emit('toggle-add-task')" 
      :text="showAddTask ? 'Close' : 'Add Task'" 
      :color="showAddTask ? 'red' : 'green'" 
      />
  </header>
</template>

<script>
import Button from "./Button";

export default {
  name: "Header",
  props: {
    title: String,
    showAddTask: Boolean,
  },
  components: {
    Button,
  },
  computed: {
    homepage() {
      return this.$route.path === '/';
    }
  }
};
</script>

